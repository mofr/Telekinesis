package org.telekinesis.client;

import com.google.gson.annotations.SerializedName;

public enum KeyboardKey {
    @SerializedName("AltTab") ALT_TAB,
    @SerializedName("VolumeMute") VOLUME_MUTE,
    @SerializedName("VolumeUp") VOLUME_UP,
    @SerializedName("VolumeDown") VOLUME_DOWN,
    @SerializedName("Left") LEFT,
    @SerializedName("Right") RIGHT,
    @SerializedName("Up") UP,
    @SerializedName("Down") DOWN,
    @SerializedName("Space") SPACE;
}
